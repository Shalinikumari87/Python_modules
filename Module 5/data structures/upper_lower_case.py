s = "hello World"
print(s.lower()) # it print all character in lower case
print(s.upper()) # it print all character in upper case
print(s.title()) # it print first letter as capital 
print(s.swapcase()) # it print uppercase char to lowercase and lowercase to uppercase
print(s.capitalize()) # it print first character into uppercase.




